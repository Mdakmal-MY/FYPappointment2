    public void viewSchedule()
    {
        model = (DefaultTableModel) scheduleTable.getModel();
        ArrayList<schedule> u = appointmentsysDAO.getSchedule(supervisorDisplay.getText());
        Object data[] = new Object[5];

        for(int i= 0; i<u.size(); i++)
        {
            data[0] = u.get(i).getDates();
            data[1] = u.get(i).getDatesStatus();
            data[2] = u.get(i).getVenue();
            data[3] = u.get(i).getTimefrom();
            data[4] = u.get(i).getTimeto();
            model.addRow(data); 
        }
    }
    
