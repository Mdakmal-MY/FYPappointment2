    private void submitBtn1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitBtn1MouseClicked

        appointment a = new appointment();
        schedule s = new schedule(supervisorDisplay.getText(), dateDisplay.getText(), toField.getText()
                                , fromField.getText(), "Available", venueField.getText());
        a.setAppID("S"+(count)+"B");
        a.setAppSubject(subjectField.getText());
        a.setAppDetail(detailField.getText());
        a.setStudentid(displayName.getText());
        a.setSchedule(s);
        a.setAppStatus("Pending");
        int status = appointmentsysDAO.makeAppt(a);
        if(status>0)
        {
            JOptionPane.showMessageDialog(null, "Registered Appt!", "Status", JOptionPane.INFORMATION_MESSAGE);
            count = count+1;
        }
    }//GEN-LAST:event_submitBtn1MouseClicked

